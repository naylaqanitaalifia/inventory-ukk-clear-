    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        try {
            $data = $request->validated();

            $emailPrefix = substr($data['email'], 0, 4);
            $nextId = User::max('id') + 1;
            $generatePassword = $emailPrefix . $nextId;

            $data['password'] = Hash::make($generatePassword);
            $data['is_password_changed'] = 0;

            User::create($data);

            if ($request->routeIs('admin.admins*')) {
                return redirect()->route('')
            }
        } catch (\Throwable $th) {
            //throw $th;
        }
    }

